  private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
    String dni = this.campoDni.getText();
    String nombreApellido = this.campoApellidoNombre.getText();
    String telefono = this.campoTelefono.getText();
    String correoElectronico = this.campoCorreoElectronico.getText();
    Paciente paciente = new Paciente(dni, nombreApellido, telefono, correoElectronico);
    this.listaPacientes.add(paciente);
    this.cargarTabla();
  }//GEN-LAST:event_botonGuardarActionPerformed

  private void botonLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonLimpiarActionPerformed
    this.campoDni.setText(null);
    this.campoApellidoNombre.setText(null);
    this.campoTelefono.setText(null);
    this.campoCorreoElectronico.setText(null);
    this.cargarTabla();
  }//GEN-LAST:event_botonLimpiarActionPerformed

  private void botonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarActionPerformed
    int index = this.tablaPacientes.getSelectedRow();
    if (index >= 0) {
      this.listaPacientes.remove(index);
      this.cargarTabla();
    }
  }//GEN-LAST:event_botonBorrarActionPerformed

  private void botonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarActionPerformed
    int index = this.tablaPacientes.getSelectedRow();
    if (index >= 0) {
      this.mostrarDatos(index);
    }
  }//GEN-LAST:event_botonMostrarActionPerformed

