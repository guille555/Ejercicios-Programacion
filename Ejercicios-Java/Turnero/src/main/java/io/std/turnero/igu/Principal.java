  private void cargarTabla() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("DNI");
    modelo.addColumn("APELLIDO Y NOMBRE");
    modelo.addColumn("TELEFONO");
    modelo.addColumn("CORREO ELECTRONICO");
    String[] informacion = new String[4];
    for (Paciente paciente : listaPacientes) {
      informacion[0] = paciente.getDni();
      informacion[1] = paciente.getApellidoNombre();
      informacion[2] = paciente.getTelefono();
      informacion[3] = paciente.getCorreoElectronico();
      modelo.addRow(informacion);
    }
    this.tablaPacientes.setModel(modelo);
  }

  private void mostrarDatos(int indice) {
    Paciente paciente = this.listaPacientes.get(indice);
    this.campoDni.setText(paciente.getDni());
    this.campoApellidoNombre.setText(paciente.getApellidoNombre());
    this.campoTelefono.setText(paciente.getTelefono());
    this.campoCorreoElectronico.setText(paciente.getCorreoElectronico());
  }

